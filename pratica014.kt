/**
 * Operadores relacionais
 */
fun main() {
    var n1:Int = 5
    var n2:Int = 4
    println("$n1 é maior que $n2? ${n1>n2}")
    println("$n1 é menor que $n2? ${n1<n2}")
    println("$n1 é maior ou igual que $n2? ${n1>=n2}")
    println("$n1 é menor ou igual que $n2? ${n1<=n2}")
    println("$n1 é igual a $n2? ${n1==n2}")
    println("$n1 é diferente que $n2? ${n1!=n2}")
}